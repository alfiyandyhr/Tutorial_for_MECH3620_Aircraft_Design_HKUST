md"""

XFOIL requires an airfoil shape and an operating condition (Reynolds number $Re$, Mach number $M$, and angle of attack $\alpha$) to determine the aerodynamic coefficients. The `xfoil_analysis` function returns two sets of results: an inviscid analysis (no drag), and a viscous analysis (with drag). 

The viscous analysis results, which depend on $\alpha$, $Re$ and $M$, are generally accurate. The inviscid analysis only depends on $\alpha$, and is primarily for comparison purposes with the viscous results. 
"""

